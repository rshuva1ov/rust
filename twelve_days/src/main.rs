fn main() {
    println!("=== Двенадцать дней Рождества ===\n");

    let days = [
        "первый", "второй", "третий", "четвертый", "пятый", "шестой",
        "седьмой", "восьмой", "девятый", "десятый", "одиннадцатый", "двенадцатый"
    ];

    let gifts = [
        "Куропатку в грушевом дереве",
        "Две горлицы",
        "Трех французских куриц",
        "Четырех зовущих птиц",
        "Пять золотых колец",
        "Шесть гусей, несущих яйца",
        "Семь плавающих лебедей",
        "Восемь доящих девушек",
        "Девять танцующих дам",
        "Десять прыгающих лордов",
        "Одиннадцать играющих на трубе",
        "Двенадцать барабанящих барабанщиков"
    ];

    for day in 0..12 {
        println!("На {} день Рождества моя любовь мне подарила:", days[day]);
        
        // Выводим подарки в обратном порядке до текущего дня (используя повторы)
        for gift_index in (0..=day).rev() {
            if gift_index == 0 && day > 0 {
                // Для первого подарка добавляем "И" при наличии других подарков
                println!("И {}", gifts[gift_index].to_lowercase());
            } else {
                println!("{}", gifts[gift_index]);
            }
        }
        
        println!();
    }
}
